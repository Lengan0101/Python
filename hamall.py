# python abs()
# hàm trả về giá trị tuyệt đối của số đã cho. nếu số là một số phức, trả về độ lớn của nó. abs()

number = -20

absolute_number = abs(number)
print(absolute_number)

# nhận giá trị tuyệt đối của một số
# random integer
integer = -20
print('Absolute value of -20 is:', abs(integer))

#random floating number
floating = -30.33
print('Absolute value of -30.33 is:', abs(floating))


# lấy độ lớn của một số phức
#random complex number
complex = (3 - 4j)
print('Magnitude of 3 - 4j is:', abs(complex))

#python bất kỳ()
# hàm trả về nếu bất kỳ phần tử nào của một phép lặp là.
# nếu không, nó trả về .any() True True False

boolean_list = ['True', 'False', 'True']
# check if any element is true
result = any(boolean_list)
print(result)

# any giá trị trả về: hàm trả về giá trị boolean: any()



